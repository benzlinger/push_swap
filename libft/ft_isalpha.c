#include "libft.h"

int	ft_isalpha(int a)
{
	if (a >= 65 && a <= 90)
	{
		return (1);
	}
	else if (a >= 97 && a <= 122)
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
